/** Узел документа: номер (1, 1.1, 1.1.1), текст, опциональные вложенные пункты */
export interface DocumentNode {
    /** Номер пункта: "1", "1.1", "1.5.1" и т.д. */
    number: string;
    /** Текст пункта (может содержать HTML-подобные вставки — рендерим как текст или через dangerouslySetInnerHTML при необходимости) */
    text: string;
    /** Вложенные пункты — отображаются с большим отступом слева */
    children?: DocumentNode[];
}

export interface DocumentTemplateProps {
    /** Заголовок документа */
    title: string;
    /** Контент документа: массив узлов с вложенностью (1, 1.1, 1.1.1 — отступ увеличивается по уровню) */
    items: DocumentNode[];
    /** Дополнительные классы для обёртки контента */
    className?: string;
}
